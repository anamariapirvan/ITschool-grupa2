public class ComparisonExercise {

    public static void main(String[] args) {
        int firstNumber = 20;
        int secondNumber = 30;
        boolean intResult = firstNumber == secondNumber;
        System.out.println("Are the integers equal? " + intResult);
        String firstString = "Hello";
        String secondString = "Hello";
        boolean stringResult = firstString.equals(secondString);
        System.out.println("Are the strings equal?" + stringResult);
    }
}
