package session3.homework;

public class LoopIncrementDecrement {
    public static void main(String[] args) {
        System.out.println("Counting up to 10: ");
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println("\nCounting down from 10: ");
        for (int j = 10; j >= 1; j--) {
            System.out.print(j + " ");
        }
    }
}
