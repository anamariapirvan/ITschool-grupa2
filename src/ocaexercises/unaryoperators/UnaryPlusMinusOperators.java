package ocaexercises.unaryoperators;

public class UnaryPlusMinusOperators {
    public static void main(String[] args) {
        int positiveValue = 10;
        int negativeValue = -20;

        // Unary plus and minus
        int positiveResult = +positiveValue;
        int negativeResult = -negativeValue;

        System.out.println("Positive value: " + positiveValue);
        System.out.println("Unary plus result: " + positiveResult);

        System.out.println(); // Empty line

        System.out.println("Negative value: " + negativeValue);
        System.out.println("Unary minus result: " + negativeResult);
    }
}

